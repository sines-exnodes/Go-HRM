package main

import (
	"context"
	"fmt"
	"log"
	"strings"
	"time"

	"github.com/gin-gonic/gin"

	"github.com/exnodes/hrm-api/internal/config"
	"github.com/exnodes/hrm-api/internal/dto"
	"github.com/exnodes/hrm-api/internal/handlers"
	"github.com/exnodes/hrm-api/internal/middleware"
	"github.com/exnodes/hrm-api/internal/permissions"
	"github.com/exnodes/hrm-api/internal/repositories"
	"github.com/exnodes/hrm-api/internal/services"
	"github.com/exnodes/hrm-api/internal/sse"

	// Generated by `make swag` — imported for side effects (registers spec
	// with swaggo). The blank import line is updated after the first swag
	// run; until then the import is satisfied by the placeholder package
	// generated alongside the docs.
	_ "github.com/exnodes/hrm-api/docs/swagger"
)

func main() {
	cfg := config.Load()

	db, err := config.ConnectDB(cfg)
	if err != nil {
		log.Fatalf("database connect failed: %v", err)
	}

	if err := config.AssertMigrationsUpToDate(db, cfg.MigrationsDir); err != nil {
		log.Fatalf("migration check failed: %v", err)
	}

	// ---- repositories ----
	userRepo := repositories.NewUserRepository(db)
	roleRepo := repositories.NewRoleRepository(db)
	employeeRepo := repositories.NewEmployeeRepository(db)
	dependentRepo := repositories.NewDependentRepository(db)
	tokenRepo := repositories.NewDeviceTokenRepository(db)
	quotaRepo := repositories.NewLeaveQuotaRepository(db)
	settingsRepo := repositories.NewNotificationSettingsRepository(db)
	departmentRepo := repositories.NewDepartmentRepository(db)
	positionRepo := repositories.NewPositionRepository(db)
	skillRepo := repositories.NewSkillRepository(db)
	employeeSkillRepo := repositories.NewEmployeeSkillRepository(db)
	labelRepo := repositories.NewLabelRepository(db)
	leaveRepo := repositories.NewLeaveRequestRepository(db)
	attendanceRepo := repositories.NewAttendanceRepository(db)
	announcementRepo := repositories.NewAnnouncementRepository(db)
	systemConfigRepo := repositories.NewSystemConfigRepository(db)
	inviteRepo := repositories.NewInviteRepository(db)
	holidayRepo := repositories.NewHolidayRepository(db)
	holidayTemplateRepo := repositories.NewHolidayTemplateRepository(db)
	passwordResetTokenRepo := repositories.NewPasswordResetTokenRepository(db)
	passwordResetOTPRepo := repositories.NewPasswordResetOTPRepository(db)

	// ---- services ----
	authSvc := services.NewAuthService(userRepo, roleRepo, services.AuthConfig{
		JWTSecret:            cfg.JWTSecret,
		AccessTTL:            time.Duration(cfg.JWTAccessTTLMinutes) * time.Minute,
		RefreshTTL:           time.Duration(cfg.JWTRefreshTTLDays) * 24 * time.Hour,
		RememberMeRefreshTTL: time.Duration(cfg.RememberMeRefreshTTLDays) * 24 * time.Hour,
		MaxFailedAttempts:    cfg.MaxFailedLoginAttempts,
		LockoutDuration:      time.Duration(cfg.AccountLockoutMinutes) * time.Minute,
	})
	seedSvc := services.NewSeedService(db, userRepo, roleRepo, employeeRepo, systemConfigRepo, services.SeedConfig{
		SuperAdminEmail:    cfg.SuperAdminEmail,
		SuperAdminPassword: cfg.SuperAdminPassword,
		SuperAdminName:     cfg.SuperAdminName,
	})

	uploadSvc, err := services.NewUploadService(context.Background(), cfg.Storage)
	if err != nil {
		log.Fatalf("upload service: %v", err)
	}
	skillSvc := services.NewSkillService(skillRepo, employeeSkillRepo, employeeRepo, uploadSvc)
	empSvc := services.NewEmployeeService(db, employeeRepo, dependentRepo, userRepo, roleRepo, departmentRepo, positionRepo, quotaRepo, uploadSvc, skillSvc)
	depSvc := services.NewDependentService(dependentRepo, employeeRepo)
	userSvc := services.NewUserService(userRepo, roleRepo, employeeRepo, tokenRepo, settingsRepo, empSvc)
	departmentSvc := services.NewDepartmentService(departmentRepo)
	positionSvc := services.NewPositionService(positionRepo)
	roleSvc := services.NewRoleService(roleRepo)
	labelSvc := services.NewLabelService(labelRepo)

	// Notifications and push are constructed early: both the leave notifier
	// here and the announcement notifier further down depend on them.
	notificationRepo := repositories.NewNotificationRepository(db)
	notificationSvc := services.NewNotificationService(notificationRepo)
	pushClient := services.NewPushClient(cfg)
	pushSvc := services.NewPushNotificationService(pushClient, tokenRepo)

	leaveNotifier := services.NewLeaveNotifier(notificationSvc, employeeRepo, pushSvc)
	leaveSvc := services.NewLeaveService(leaveRepo, employeeRepo, departmentRepo, positionRepo, quotaRepo, uploadSvc, holidayRepo, leaveNotifier)
	attendanceSvc := services.NewAttendanceService(cfg, attendanceRepo, employeeRepo, departmentRepo, positionRepo, leaveRepo)

	// Phase 7 — SSE hub singleton + announcement service + handler.
	// In-process pub/sub; horizontal scaling beyond 1 replica requires a
	// shared backplane (see internal/sse/hub.go for the SCALING LIMIT
	// comment).
	sseHub := sse.NewHub()
	defer sseHub.Stop()

	// Phase 9 — Email + Invite + Push (must be initialized before announcements).
	// All three degrade gracefully when their respective env keys are
	// empty: EmailService logs + records last_email_error; PushClient
	// becomes a no-op logger. Boot never fails on misconfig.
	emailSvc, err := services.NewEmailService(cfg)
	if err != nil {
		log.Fatalf("email service: %v", err)
	}
	annNotifier := services.NewAnnouncementNotifier(pushSvc, emailSvc, userRepo, notificationSvc)
	announcementSvc := services.NewAnnouncementService(
		announcementRepo, employeeRepo, departmentRepo, labelRepo,
		sseHubAdapter{hub: sseHub},
		annNotifier,
	)
	orgSettingsSvc := services.NewOrganizationSettingsService(systemConfigRepo, employeeRepo)
	inviteSvc := services.NewInviteService(cfg, inviteRepo, employeeRepo, userRepo, roleRepo, empSvc, emailSvc, db)

	userContractRepo := repositories.NewUserContractRepository(db)
	userContractSvc := services.NewUserContractService(userContractRepo, employeeRepo, uploadSvc)
	holidaySvc := services.NewHolidayService(holidayRepo, holidayTemplateRepo, leaveRepo)
	workdaySvc := services.NewWorkdayService(holidayRepo)
	passwordResetSvc := services.NewPasswordResetService(userRepo, passwordResetTokenRepo, emailSvc, cfg)
	passwordResetOTPSvc := services.NewPasswordResetOTPService(userRepo, passwordResetOTPRepo, passwordResetTokenRepo, emailSvc, cfg)
	dashboardSvc := services.NewDashboardService(cfg, employeeRepo, leaveRepo, quotaRepo, attendanceRepo, announcementRepo, holidayRepo, notificationRepo)

	// ---- run idempotent seed on boot ----
	if err := seedSvc.Seed(context.Background()); err != nil {
		log.Fatalf("seed: %v", err)
	}

	// ---- handlers ----
	authH := handlers.NewAuthHandler(authSvc, passwordResetSvc, passwordResetOTPSvc)
	roleH := handlers.NewRoleHandler(roleSvc)
	empH := handlers.NewEmployeeHandler(empSvc, passwordResetSvc)
	depH := handlers.NewDependentHandler(depSvc)
	userH := handlers.NewUserHandler(userSvc, passwordResetSvc)
	departmentH := handlers.NewDepartmentHandler(departmentSvc)
	positionH := handlers.NewPositionHandler(positionSvc)
	skillH := handlers.NewSkillHandler(skillSvc)
	labelH := handlers.NewLabelHandler(labelSvc)
	leaveH := handlers.NewLeaveHandler(leaveSvc)
	attendanceH := handlers.NewAttendanceHandler(attendanceSvc)
	announcementH := handlers.NewAnnouncementHandler(announcementSvc)
	sseH := handlers.NewSSEHandler(sseHub)
	orgSettingsH := handlers.NewOrganizationSettingsHandler(orgSettingsSvc)
	inviteH := handlers.NewInviteHandler(inviteSvc)
	notifH := handlers.NewNotificationHandler(pushSvc, notificationSvc)
	userContractH := handlers.NewUserContractHandler(userContractSvc)
	holidayH := handlers.NewHolidayHandler(holidaySvc)
	workdayH := handlers.NewWorkdayHandler(workdaySvc)
	dashboardH := handlers.NewDashboardHandler(dashboardSvc)

	// ---- CORS origin allow-list ----
	var corsOrigins []string
	for _, o := range strings.Split(cfg.CORSAllowedOrigins, ",") {
		if o = strings.TrimSpace(o); o != "" {
			corsOrigins = append(corsOrigins, o)
		}
	}
	if len(corsOrigins) == 0 && cfg.AppEnv == "production" {
		log.Fatal("config: CORS_ALLOWED_ORIGINS must be set in production")
	}

	gin.SetMode(cfg.GinMode)
	r := gin.New()

	// Install custom struct-tag validators (e.g. `optemail`) onto gin's
	// binding engine before any route is served.
	if err := dto.RegisterValidators(); err != nil {
		log.Fatalf("register validators: %v", err)
	}

	// Middleware order matters: Recovery first (catches panics from later
	// middleware too), then logger, then CORS, then ErrorHandler (writes
	// the envelope at the end of the chain).
	r.Use(middleware.Recovery())
	r.Use(gin.Logger())
	r.Use(middleware.CORS(corsOrigins, cfg.AppEnv))
	r.Use(middleware.ErrorHandler())

	handlers.RegisterRoutes(r, handlers.NewHandlers(), cfg.SwaggerEnabled)

	// ---- routes ----
	v1 := r.Group("/api/v1")
	{
		// Public auth endpoints
		auth := v1.Group("/auth")
		auth.POST("/login", authH.Login)
		auth.POST("/refresh", authH.Refresh)
		auth.POST("/forgot-password", authH.ForgotPassword)
		auth.GET("/verify-token", authH.VerifyResetToken)
		auth.POST("/reset-password", authH.ResetPassword)
		auth.POST("/set-password", authH.ResetPassword) // Python parity alias

		// Mobile forgot-password (DR-001-001-02). OTP-based: the app mails a
		// 6-digit code instead of the web flow's one-click link. Screen 3
		// finishes on the shared /auth/reset-password above, using the
		// reset_token that /mobile/verify-otp returns.
		authMobile := auth.Group("/mobile")
		authMobile.POST("/forgot-password", authH.MobileForgotPassword) // also backs "Resend Code"
		authMobile.POST("/verify-otp", authH.MobileVerifyOTP)

		// ---- /invites/accept (Phase 9 — PUBLIC) ----
		// The token in the body is the credential. Lives outside the
		// JWT-protected group because the invitee has no account yet.
		v1.POST("/invites/accept", inviteH.Accept)

		// Protected endpoints
		authed := v1.Group("")
		authed.Use(middleware.JWT(userRepo, cfg.JWTSecret))

		authed.POST("/auth/logout", authH.Logout)
		authed.GET("/dashboard", dashboardH.Get)

		// ---- /roles ----
		roles := authed.Group("/roles")
		roles.GET("/permissions", middleware.RequirePerms(authSvc, permissions.PermRolesRead), roleH.ListPermissions)
		roles.GET("", middleware.RequirePerms(authSvc, permissions.PermRolesRead), roleH.List)
		roles.POST("", middleware.RequirePerms(authSvc, permissions.PermRolesCreate), roleH.Create)
		roles.GET(":id", middleware.RequirePerms(authSvc, permissions.PermRolesRead), roleH.Get)
		roles.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermRolesUpdate), roleH.Update)
		roles.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermRolesDelete), roleH.Delete)

		// ---- /users/me* self-service (auth only) ----
		authed.GET("/users/me", userH.GetMe)
		authed.POST("/users/me/change-password", userH.ChangeMyPassword)
		authed.POST("/users/me/change-email", userH.ChangeMyEmail)
		authed.POST("/users/me/device-tokens", userH.RegisterDeviceToken)
		authed.DELETE("/users/me/device-tokens/:token", userH.RemoveDeviceToken)
		authed.PATCH("/users/me/notification-settings", userH.UpdateMyNotificationSettings)

		// ---- /users/:id admin ----
		adminUsers := authed.Group("/users")
		adminUsers.GET("", middleware.RequirePerms(authSvc, permissions.PermUsersRead), userH.List)
		adminUsers.GET(":id", middleware.RequirePerms(authSvc, permissions.PermUsersRead), userH.Get)
		adminUsers.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermUsersUpdate), userH.AdminPatch)
		adminUsers.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermUsersDelete), userH.AdminDelete)
		adminUsers.PATCH(":id/change-password", middleware.RequirePerms(authSvc, permissions.PermUsersChangePwd), userH.AdminChangePassword)
		adminUsers.POST(":id/change-email", middleware.RequirePerms(authSvc, permissions.PermUsersUpdate), userH.AdminChangeEmail)
		adminUsers.PUT(":id/roles", middleware.RequirePerms(authSvc, permissions.PermUsersManageRoles), userH.AssignRoles)
		adminUsers.POST(":id/reset-password", middleware.RequirePerms(authSvc, permissions.PermUsersChangePwd), userH.AdminSendResetLink)
		adminUsers.POST(":id/send-invite", middleware.RequirePerms(authSvc, permissions.PermUsersCreate), userH.AdminSendInvite)

		// ---- /users/:id/contracts ----
		// IMPORTANT: :id must match the existing adminUsers wildcard name.
		// Router-level gate: PermUsersContractsView on all routes (read gate);
		// write routes additionally require PermUsersContractsManage (defense-in-depth).
		userContracts := authed.Group("/users/:id/contracts")
		userContracts.Use(middleware.RequirePerms(authSvc, permissions.PermUsersContractsView))
		userContracts.GET("", userContractH.List)
		userContracts.GET(":contractID", userContractH.Get)
		userContracts.POST("", middleware.RequirePerms(authSvc, permissions.PermUsersContractsManage), userContractH.Create)
		userContracts.PATCH(":contractID", middleware.RequirePerms(authSvc, permissions.PermUsersContractsManage), userContractH.Update)
		userContracts.DELETE(":contractID", middleware.RequirePerms(authSvc, permissions.PermUsersContractsManage), userContractH.Delete)
		userContracts.POST(":contractID/attachment", middleware.RequirePerms(authSvc, permissions.PermUsersContractsManage), userContractH.UploadAttachment)

		// ---- /holidays ----
		// Static sub-paths (/years, /templates, /import) must be registered
		// BEFORE the /:id wildcard routes to avoid Gin routing conflicts.
		holidays := authed.Group("/holidays")
		holidays.GET("/years", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysView), holidayH.GetYears)
		holidays.GET("/templates", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysManage), holidayH.ListTemplates)
		holidays.POST("/import", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysManage), holidayH.Import)
		holidays.GET("", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysView), holidayH.List)
		holidays.POST("", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysManage), holidayH.Create)
		holidays.PATCH("/:id", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysManage), holidayH.Update)
		holidays.DELETE("/:id", middleware.RequirePerms(authSvc, permissions.PermOrgHolidaysManage), holidayH.Delete)

		// ---- /workdays ----
		authed.GET("/workdays", middleware.RequirePerms(authSvc, permissions.PermOrgWorkdaysView), workdayH.GetYear)

		// ---- /employees/me* self-service (auth only) ----
		authed.GET("/employees/me", empH.GetMe)
		authed.PATCH("/employees/me", empH.UpdateMe)
		authed.PATCH("/employees/me/avatar", empH.UpdateMyAvatar)

		// ---- /employees admin ----
		adminEmps := authed.Group("/employees")
		adminEmps.GET("", middleware.RequirePerms(authSvc, permissions.PermEmployeesRead), empH.List)
		adminEmps.POST("", middleware.RequirePerms(authSvc, permissions.PermEmployeesCreate), empH.Create)
		// Static segments registered alongside :id — gin's radix tree prioritizes
		// the literals over the :id wildcard (employees parity #10).
		adminEmps.GET("/manager-candidates", middleware.RequirePerms(authSvc, permissions.PermEmployeesRead), empH.ManagerCandidates)
		adminEmps.POST("/import", middleware.RequirePerms(authSvc, permissions.PermEmployeesCreate), empH.Import)
		adminEmps.GET("/import/template", middleware.RequirePerms(authSvc, permissions.PermEmployeesCreate), empH.ImportTemplate)
		adminEmps.GET(":id", middleware.RequirePerms(authSvc, permissions.PermEmployeesRead), empH.Get)
		adminEmps.GET(":id/direct-reports", middleware.RequirePerms(authSvc, permissions.PermEmployeesRead), empH.DirectReports)
		adminEmps.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermEmployeesUpdate), empH.Update)
		adminEmps.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermEmployeesDelete), empH.Delete)
		adminEmps.PATCH(":id/avatar", middleware.RequirePerms(authSvc, permissions.PermEmployeesUpdate), empH.UpdateAvatarAdmin)
		adminEmps.PATCH(":id/leave-quota", middleware.RequirePerms(authSvc, permissions.PermLeaveQuotaManage), empH.UpdateLeaveQuota)

		// ---- /employees/:id/dependents (owner or PermDependentsManage — enforced in handler) ----
		// NOTE: gin requires a single wildcard name per path position, so the
		// employee segment uses :id (shared with the admin /employees/:id tree)
		// and the nested dependent uses :dependentID.
		authed.GET("/employees/:id/dependents", depH.List)
		authed.POST("/employees/:id/dependents", depH.Create)
		authed.PATCH("/employees/:id/dependents/:dependentID", depH.Update)
		authed.DELETE("/employees/:id/dependents/:dependentID", depH.Delete)

		// ---- /departments ----
		departments := authed.Group("/departments")
		departments.GET("", middleware.RequirePerms(authSvc, permissions.PermDepartmentsRead), departmentH.List)
		departments.POST("", middleware.RequirePerms(authSvc, permissions.PermDepartmentsCreate), departmentH.Create)
		departments.GET(":id", middleware.RequirePerms(authSvc, permissions.PermDepartmentsRead), departmentH.Get)
		departments.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermDepartmentsUpdate), departmentH.Update)
		departments.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermDepartmentsDelete), departmentH.Delete)

		// ---- /positions ----
		positions := authed.Group("/positions")
		positions.GET("", middleware.RequirePerms(authSvc, permissions.PermPositionsRead), positionH.List)
		positions.POST("", middleware.RequirePerms(authSvc, permissions.PermPositionsCreate), positionH.Create)
		positions.GET(":id", middleware.RequirePerms(authSvc, permissions.PermPositionsRead), positionH.Get)
		positions.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermPositionsUpdate), positionH.Update)
		positions.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermPositionsDelete), positionH.Delete)

		// ---- /skills (catalog) ----
		skills := authed.Group("/skills")
		skills.GET("", middleware.RequirePerms(authSvc, permissions.PermSkillsRead), skillH.List)
		skills.POST("", middleware.RequirePerms(authSvc, permissions.PermSkillsCreate), skillH.Create)
		skills.GET(":id", middleware.RequirePerms(authSvc, permissions.PermSkillsRead), skillH.Get)
		skills.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermSkillsUpdate), skillH.Update)
		skills.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermSkillsDelete), skillH.Delete)

		// ---- /employees/:id/skills (assignment, nested) ----
		// Read uses PermEmployeesRead, write uses PermEmployeesUpdate — the
		// employee is the aggregate root for the skill list, so this matches
		// the existing employees-perms model.
		authed.GET("/employees/:id/skills", middleware.RequirePerms(authSvc, permissions.PermEmployeesRead), skillH.ListForEmployee)
		authed.PUT("/employees/:id/skills", middleware.RequirePerms(authSvc, permissions.PermEmployeesUpdate), skillH.ReplaceForEmployee)

		// ---- /announcement-labels (announcement label catalog) ----
		// Only two endpoints per REVISION NOTES item #4. Both gated by
		// PermAnnounceManage (the Admin + HR Manager roles received this
		// permission in seed_service.go as part of Phase 4).
		labels := authed.Group("/announcement-labels")
		labels.GET("", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), labelH.List)
		labels.POST("", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), labelH.GetOrCreate)

		// ---- /leave-requests (Phase 5) ----
		// Self-service routes (auth only) come first so the static
		// /dashboard/me and /history/me segments aren't swallowed by the
		// /:id route below. /balance/:employee_id is also gated by
		// PermLeaveRead (anyone with read can look up anyone's balance —
		// matches the Python contract).
		leaves := authed.Group("/leave-requests")
		leaves.GET("/dashboard/me", leaveH.GetMyDashboard)
		leaves.GET("/history/me", leaveH.ListMyHistory)
		leaves.GET("/balance/:employee_id", middleware.RequirePerms(authSvc, permissions.PermLeaveRead), leaveH.GetBalance)
		leaves.GET("", middleware.RequirePerms(authSvc, permissions.PermLeaveRead), leaveH.List)
		leaves.POST("", middleware.RequirePerms(authSvc, permissions.PermLeaveCreate), leaveH.Create)
		leaves.GET("export", middleware.RequirePerms(authSvc, permissions.PermLeaveRead), leaveH.Export)
		leaves.GET(":id", middleware.RequirePerms(authSvc, permissions.PermLeaveRead), leaveH.Get)
		leaves.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermLeaveUpdate), leaveH.Update)
		leaves.POST(":id/approve", leaveH.Approve)
		leaves.POST(":id/reject", leaveH.Reject)
		leaves.POST(":id/cancel", middleware.RequirePerms(authSvc, permissions.PermLeaveCancel), leaveH.Cancel)
		// POST (not DELETE) per Python source — REVISION NOTES item #5.
		leaves.POST(":id/delete", middleware.RequirePerms(authSvc, permissions.PermLeaveDelete), leaveH.Delete)

		// ---- /announcements (Phase 7 — web) ----
		// Read routes require the source module view permission and remain
		// visibility-filtered server-side; PermAnnounceManage gates writes.
		announce := authed.Group("/announcements")
		announce.GET("", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.List)
		announce.GET(":id", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.Get)
		announce.POST(":id/view", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.MarkViewed)
		announce.POST("", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), announcementH.Create)
		announce.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), announcementH.Update)
		announce.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), announcementH.Delete)
		announce.POST(":id/publish", middleware.RequirePerms(authSvc, permissions.PermAnnounceManage), announcementH.Publish)

		// ---- /mobile/announcements (Phase 7 — mobile) ----
		// Read permission required; service forces visibility filtering. Description
		// omitted from list items; detail fetch via :id.
		// `` → top-5 unpaginated brief (home widget — matches Python's
		// GET /mobile/announcements/).
		// `/list` → paginated brief.
		mobileAnnounce := authed.Group("/mobile/announcements")
		mobileAnnounce.GET("", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.MobileBrief)
		mobileAnnounce.GET("/list", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.MobileList)
		mobileAnnounce.GET(":id", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.MobileGet)
		mobileAnnounce.POST(":id/read", middleware.RequirePerms(authSvc, permissions.PermAnnounceRead), announcementH.MarkViewed)

		// ---- /mobile/notifications (EP-005 — in-app notification feed) ----
		// JWT-only, no permission gate: every authenticated employee is
		// entitled to their own notifications by definition, and the service
		// scopes every query to the caller's user_id. A notifications:read
		// permission would have to be seeded to all five roles immediately,
		// making it a permission that can never be false.
		//
		// Gin route precedence: the literal /unread-count is registered
		// before any wildcard segment on this group.
		mobileNotif := authed.Group("/mobile/notifications")
		mobileNotif.GET("/unread-count", notifH.UnreadCount)
		mobileNotif.GET("", notifH.List)
		mobileNotif.POST(":id/read", notifH.MarkRead)

		// ---- /attendance (Phase 6) ----
		// Gin route precedence: literal segments (today/me/matrix) must be
		// registered BEFORE the :id wildcard, otherwise the literal text
		// is routed through Get(id="today"). Within a Group(), gin
		// evaluates in registration order — keep this ordering intact.
		attendance := authed.Group("/attendance")
		// Self-service (JWT-only — no perm gate).
		attendance.POST("/check-in", attendanceH.CheckIn)
		attendance.POST("/check-out", attendanceH.CheckOut)
		attendance.GET("/today", attendanceH.Today)
		attendance.GET("/me", attendanceH.Me)
		// Permissioned reads. Non-managers are silently scoped to own
		// rows by the service (matches Python contract).
		// D1: root path serves the monthly matrix (Python/FE parity).
		attendance.GET("", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.Matrix)
		// /matrix kept as an explicit alias.
		attendance.GET("/matrix", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.Matrix)
		// Flat paginated rows (Go-only convenience; not the BA matrix).
		attendance.GET("/records", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.List)
		attendance.GET(":id", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.Get)
		// Excel export (bulk + single). /export is a static literal so gin
		// resolves it before the :id wildcard at the same depth. The nested
		// /export/:employee_id is one depth deeper and therefore unambiguous.
		attendance.GET("/export", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.Export)
		attendance.GET("/export/:employee_id", middleware.RequirePerms(authSvc, permissions.PermAttendanceRead), attendanceH.ExportEmployee)
		// Admin manage.
		attendance.POST("", middleware.RequirePerms(authSvc, permissions.PermAttendanceManage), attendanceH.AdminCreate)
		attendance.PATCH(":id", middleware.RequirePerms(authSvc, permissions.PermAttendanceManage), attendanceH.AdminUpdate)
		attendance.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermAttendanceManage), attendanceH.AdminDelete)
		attendance.POST("/auto-checkout", middleware.RequirePerms(authSvc, permissions.PermAttendanceManage), attendanceH.AutoCheckOut)

		// ---- /organization-settings (Phase 8) ----
		// Attendance subtree is admin-only (RequirePerms). Company-profile
		// GET is JWT-only so the mobile map-preview can render for any
		// signed-in user; PATCH stays admin-only.
		org := authed.Group("/organization-settings")
		org.GET("/attendance", middleware.RequirePerms(authSvc, permissions.PermOrgSettings), orgSettingsH.GetAttendance)
		org.PATCH("/attendance", middleware.RequirePerms(authSvc, permissions.PermOrgSettings), orgSettingsH.UpdateAttendance)
		org.GET("/company-profile", orgSettingsH.GetCompanyProfile)
		org.PATCH("/company-profile", middleware.RequirePerms(authSvc, permissions.PermOrgSettings), orgSettingsH.UpdateCompanyProfile)

		// ---- /invites (Phase 9 — admin endpoints) ----
		// Public accept is registered above (outside the authed group).
		invites := authed.Group("/invites")
		invites.GET("", middleware.RequirePerms(authSvc, permissions.PermInviteManage), inviteH.List)
		invites.POST("", middleware.RequirePerms(authSvc, permissions.PermInviteManage), inviteH.Create)
		invites.GET(":id", middleware.RequirePerms(authSvc, permissions.PermInviteManage), inviteH.Get)
		invites.POST(":id/resend", middleware.RequirePerms(authSvc, permissions.PermInviteManage), inviteH.Resend)
		invites.DELETE(":id", middleware.RequirePerms(authSvc, permissions.PermInviteManage), inviteH.Revoke)

		// ---- /notifications/test (Phase 9 — admin debug push) ----
		// Gated by PermUsersManageRoles per REVISION NOTES #19 — closest
		// fit for a privileged debug surface. Pushes only to the calling
		// user's own device tokens (self-test).
		authed.POST("/notifications/test", middleware.RequirePerms(authSvc, permissions.PermUsersManageRoles), notifH.SendTest)

		// ---- /sse (Phase 7 — Server-Sent Events) ----
		// Uses a separate JWT middleware that also accepts ?token= because
		// EventSource cannot set Authorization headers (REVISION NOTES #8).
		sseGroup := v1.Group("/sse")
		sseGroup.Use(middleware.JWTFromQueryOrHeader(userRepo, cfg.JWTSecret))
		sseGroup.GET("/announcements", sseH.Stream)
	}

	// Daily in-process auto-checkout. Closes any session left open at
	// AutoCheckoutHour:00 company time. Idempotent, so a second replica
	// double-firing is harmless. context.Background() = process-lifetime;
	// the process has no graceful-shutdown path to cancel it through today.
	if cfg.AutoCheckoutEnabled {
		go services.StartAutoCheckoutScheduler(context.Background(), attendanceSvc, cfg.CompanyTimezone, cfg.AutoCheckoutHour)
	} else {
		log.Printf("auto-checkout: scheduler disabled (ATTENDANCE_AUTO_CHECKOUT_ENABLED=false)")
	}

	addr := fmt.Sprintf(":%s", cfg.Port)
	log.Printf("exnodes-hrm-api listening on %s (env=%s, swagger=%t)", addr, cfg.AppEnv, cfg.SwaggerEnabled)
	if err := r.Run(addr); err != nil {
		log.Fatalf("server failed: %v", err)
	}
}
